import { useCallback } from "react";

import type { ReactNode } from "react";

interface NotificationBellProps {
  count: number;
  onClick: () => void;
}

export function NotificationBell({
  count,
  onClick,
}: NotificationBellProps): ReactNode {
  const handleClick = useCallback((): void => {
    onClick();
  }, [onClick]);

  return (
    <button
      type="button"
      className="relative min-h-11 min-w-11 flex items-center justify-center rounded-full transition-colors active:bg-bg-surface-hover"
      onClick={handleClick}
      aria-label="通知"
    >
      <svg
        className="w-6 h-6 text-text-primary"
        fill="none"
        viewBox="0 0 24 24"
        stroke="currentColor"
        strokeWidth={2}
      >
        <path
          strokeLinecap="round"
          strokeLinejoin="round"
          d="M15 17h5l-1.405-1.405A2.032 2.032 0 0118 14.158V11a6.002 6.002 0 00-4-5.659V5a2 2 0 10-4 0v.341C7.67 6.165 6 8.388 6 11v3.159c0 .538-.214 1.055-.595 1.436L4 17h5m6 0v1a3 3 0 11-6 0v-1m6 0H9"
        />
      </svg>
      {count > 0 && (
        <span className="absolute -top-0.5 -right-0.5 min-w-5 h-5 flex items-center justify-center rounded-full bg-error text-text-on-accent text-xs font-bold px-1">
          {count}
        </span>
      )}
    </button>
  );
}
